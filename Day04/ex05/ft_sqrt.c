int	ft_sqrt(int nb)
{
	int sqrt;

	sqrt = 1;
	while ((sqrt * sqrt) <= nb)
	{
		if ((sqrt * sqrt) == nb)
			return (sqrt);
		else
			sqrt++;
	}
	return (0);
}
