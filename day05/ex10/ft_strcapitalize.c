char	*ft_strcapitalize(char *str)
{
	int i;

	i = 0;
	if (str[i] != '\0' && str[i] >= 'a' && str[i] <= 'z')
	{
		str[i] -= 32;
		i++;
	}
	while (str[i] != '\0')
	{
		if (!((str[i - 1] >= '0' && str[i - 1] <= '9') ||
			(str[i - 1] >= 'A' && str[i - 1] <= 'Z') ||
			(str[i - 1] >= 'a' && str[i - 1] <= 'z')))
		{
			if (str[i] >= 'a' && str[i] <= 'z')
				str[i] += +'A' - 'a';
		}
		else if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] += -'A' + 'a';
		++i;
	}
	return (str);
}
